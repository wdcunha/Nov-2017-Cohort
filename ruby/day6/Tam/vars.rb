# Variables in Ruby follow the snake case convention, for instance
first_name = 'Tam'

# remember that variable names can contain numbers but numbes can't be the first
# character in the variables
a1 = 'John' # this is good
1a = 'John' # this will throw an error

# this is called `screaming case` and it's used to define constants in your
# application where you don't expect the value to change
